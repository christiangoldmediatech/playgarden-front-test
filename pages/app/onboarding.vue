<template>
  <v-container fluid>
    <v-row no-gutters>
      <v-col class="text-center mt-4" cols="12">
        <underlined-title
          class="text-h4 text-md-h3"
          text="Learn how to use Playgarden Prep Online"
        />
      </v-col>
      <v-col cols="12">
        <pg-loading v-if="loading" />

        <v-card v-else elevation="0">
          <v-stepper v-if="!none" v-model="step" class="elevation-0">
            <v-stepper-header v-if="!single">
              <template v-for="({ name, description }, indexOH) in onboardings">
                <v-divider v-if="indexOH" :key="`divider-${indexOH}`" />

                <v-stepper-step
                  :key="`step-header-${indexOH}`"
                  :complete="step > indexOH + 1"
                  :step="indexOH + 1"
                >
                  {{ name }}

                  <small>{{ description }}</small>
                </v-stepper-step>
              </template>
            </v-stepper-header>

            <v-stepper-items>
              <v-stepper-content
                v-for="({ videos }, indexOI) in onboardings"
                :key="indexOI"
                class="pa-4"
                :step="indexOI + 1"
              >
                <pg-inline-video-player
                  @ready="onPlayerReady({ player: $event, videos })"
                />
              </v-stepper-content>
            </v-stepper-items>
          </v-stepper>

          <v-card-actions v-if="!none">
            <v-spacer />

            <template v-if="!single">
              <v-btn
                color="accent"
                :disabled="first"
                large
                :loading="finishing"
                text
                @click="step--"
              >
                PREV
              </v-btn>

              <v-btn v-if="!last" color="primary" large text @click="step++">
                NEXT
              </v-btn>
            </template>

            <v-btn
              v-if="single || last"
              class="text-h6 mt-n6"
              color="primary"
              :loading="finishing"
              text
              @click="onFinish"
            >
              GO TO LESSONS
            </v-btn>
          </v-card-actions>
        </v-card>
      </v-col>
    </v-row>
  </v-container>
</template>

<script>
import { mapActions } from 'vuex'
// import PgInlineVideoPlayer from '@/components/pg-video-js-player/PgInlineVideoPlayer.vue'

export default {
  name: 'Onboarding',

  // components: {
  //   PgInlineVideoPlayer
  // },

  data: () => ({
    loading: true,
    finishing: false,
    step: 1,
    onboardings: [],
    player: null
  }),

  computed: {
    first () {
      return this.step === 1
    },

    last () {
      return this.step === this.steps
    },

    none () {
      return !this.onboardings.length
    },

    single () {
      return this.onboardings.length === 1
    },

    steps () {
      return this.onboardings.length
    }
  },

  async created () {
    try {
      const onboardings = await this.getOnboardings()

      if (Array.isArray(onboardings) && onboardings.length > 0) {
        this.onboardings = [onboardings[0]]
      } else {
        this.onboardings = []
      }
    } catch (e) {
    } finally {
      setTimeout(() => {
        if (this.none) {
          this.onFinish()
        } else {
          this.loading = false
        }
      }, 250)
    }
  },

  beforeDestroy () {
    this.player = null
  },

  methods: {
    ...mapActions('auth', ['updateAuthOnboarding']),
    ...mapActions('onboarding', ['getOnboardings']),

    onPlayerReady ({ player, videos }) {
      this.player = player

      const interval = window.setInterval(() => {
        if (this.onboardings.length) {
          // const { videos } = this.onboardings[0]
          this.player.loadPlaylist([
            {
              videoId: 1,
              title: videos.name,
              poster: videos.thumbnail,
              src: {
                src: videos.videoUrl.HLS,
                type: 'application/x-mpegURL'
              }
            }
          ], 0)
          window.clearInterval(interval)
        }
      }, 50)
    },

    nextStep () {
      this.$router.push({ name: 'app-dashboard' })
      // if (this.step < this.steps) {
      //   this.step++
      // }
    },

    async onFinish () {
      this.finishing = true

      try {
        await this.updateAuthOnboarding()

        this.$router.push({ name: 'app-dashboard' })
      } catch (e) {
      } finally {
        this.finishing = false
      }
    }
  }
}
</script>
