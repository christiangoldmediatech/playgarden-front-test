<template>
  <v-dialog
    v-model="dialog"
    :fullscreen="$vuetify.breakpoint.xs"
    max-width="500px"
    persistent
  >
    <v-card>
      <v-toolbar
        class="flex-grow-0"
        color="primary darken-1"
        dark
        dense
        flat
      >
        <v-toolbar-title>
          {{ title }}
        </v-toolbar-title>

        <v-spacer />

        <v-btn
          :disabled="loading"
          icon
          @click.stop="close"
        >
          <v-icon>
            mdi-close
          </v-icon>
        </v-btn>
      </v-toolbar>

      <validation-observer ref="obs" v-slot="{ invalid, passes }">
        <v-card-text>
          <v-container>
            <v-form
              ref="activityTypeForm"
              @submit.prevent="passes(save)"
            >
              <validation-provider v-slot="{ errors }" name="Activity Type Name" rules="required">
                <v-text-field
                  v-model="item.name"
                  :error-messages="errors"
                  label="Name"
                  solo
                />
              </validation-provider>

              <validation-provider v-slot="{ errors }" name="Activity Type Description" rules="required">
                <v-textarea
                  v-model="item.description"
                  :error-messages="errors"
                  label="Description"
                  solo
                />
              </validation-provider>

              <span class="subtitle-2">Type:</span>

              <v-row justify="space-around">
                <v-checkbox
                  v-model="item.extra"
                  class="mt-1"
                  dense
                  label="Extra"
                />

                <v-checkbox
                  v-model="item.lesson"
                  class="mt-1"
                  dense
                  label="Lesson"
                />

                <v-checkbox
                  v-model="item.activity"
                  class="mt-1"
                  dense
                  label="Activity"
                />
              </v-row>

              <span class="subtitle-2">Color:</span>

              <v-row justify="center">
                <v-color-picker
                  v-model="item.color"
                  hide-mode-switch
                  hide_inputs
                  mode="hexa"
                  :show-swatches="false"
                />
              </v-row>

              <p class="mb-5 subtitle-2">
                Icon:
              </p>

              <v-row v-if="item.icon" class="mb-5" justify="center">
                <v-col cols="5" sm="3">
                  <v-img
                    contain
                    :src="item.icon"
                  />
                </v-col>
              </v-row>

              <v-row>
                <file-uploader
                  ref="fileUploader"
                  :file.sync="file"
                  label="Upload Icon"
                  mode="image"
                  path="activity-type"
                  placeholder="Select an icon for this activity type"
                  prepend-icon="mdi-camera"
                  gif
                  png
                  svg
                />
              </v-row>
            </v-form>
          </v-container>
        </v-card-text>

        <v-divider />

        <v-card-actions>
          <v-spacer />
          <v-btn
            class="white--text"
            color="green"
            :disabled="invalid"
            :loading="loading"
            :text="$vuetify.breakpoint.smAndUp"
            @click.stop="passes(save)"
          >
            Save
          </v-btn>
          <v-btn
            class="white--text"
            color="red"
            :disabled="loading"
            :text="$vuetify.breakpoint.smAndUp"
            @click.stop="close"
          >
            Cancel
          </v-btn>
        </v-card-actions>
      </validation-observer>
    </v-card>
  </v-dialog>
</template>

<script>
import { mapActions } from 'vuex'

function generateItemTemplate () {
  return {
    name: '',
    description: '',
    lesson: false,
    extra: false,
    activity: false,
    color: '#FF0000',
    icon: null
  }
}

export default {
  name: 'ActivityTypeEditorDialog',

  data () {
    return {
      file: null,
      dialog: false,
      loading: false,
      valid: true,
      id: null,
      item: generateItemTemplate()
    }
  },

  computed: {
    title () {
      return this.id === null ? 'New Activity Type' : 'Edit Activity Type'
    }
  },

  methods: {
    ...mapActions('admin/activity', [
      'createType',
      'updateType',
      'getTypes'
    ]),

    close () {
      this.$nextTick(() => {
        this.dialog = false
        this.loading = false
        this.$refs.obs.reset()
      })
    },

    async save () {
      this.loading = true
      try {
        const icon = await this.$refs.fileUploader.handleUpload()
        this.item.icon = icon
        if (this.id === null) {
          await this.createType(this.item)
        } else {
          await this.updateType({ id: this.id, data: this.item })
        }
        await this.getTypes()
      } catch (err) {
        this.loading = false
        return
      } finally {
        this.close()
      }
    },

    resetItem () {
      this.id = null
      this.item = generateItemTemplate()
      this.file = null
    },

    loadItem (item) {
      this.id = item.id

      // Handle keys
      Object.keys(item).forEach((key) => {
        if (Object.prototype.hasOwnProperty.call(this.item, key)) {
          this.item[key] = item[key]
        }
      })

      // Handle types
      Object.keys(item.type).forEach((key) => {
        if (Object.prototype.hasOwnProperty.call(this.item, key)) {
          this.item[key] = item.type[key]
        }
      })
    },

    open (evt, item = null) {
      this.resetItem()

      if (item) {
        this.loadItem(item)
      }

      this.$nextTick(() => {
        this.dialog = true
      })
    }
  }
}
</script>
